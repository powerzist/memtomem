"""Regression tests for the `mm init` wizard (cli/init_cmd.py).

These tests pin down the shape of the `.mcp.json` generated by the wizard and
guard against a historical bug where MEMTOMEM_INDEXING__MEMORY_DIRS was written
as a plain string — which crashes the MCP server at startup because
pydantic-settings parses list-typed env vars as JSON arrays.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Literal

import pytest

from memtomem.cli.init_cmd import MmBinaryOrigin, RuntimeProfile, _write_mcp_json
from memtomem.config import Mem2MemConfig


def test_write_mcp_json_omits_env_when_empty(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    """When no env overrides are supplied, the generated .mcp.json must not
    include an empty or invalid `env` block. Settings come from
    ~/.memtomem/config.json at runtime instead."""
    monkeypatch.chdir(tmp_path)
    _write_mcp_json("uvx", ["--from", "memtomem", "memtomem-server"], {})

    data = json.loads((tmp_path / ".mcp.json").read_text(encoding="utf-8"))
    server = data["mcpServers"]["memtomem"]
    assert server["command"] == "uvx"
    assert server["args"] == ["--from", "memtomem", "memtomem-server"]
    assert "env" not in server, (
        "wizard must not write an empty/invalid env block — regression for "
        "the MEMTOMEM_INDEXING__MEMORY_DIRS plain-string bug"
    )


def test_write_mcp_json_preserves_valid_env(
    tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    """If a caller does pass env overrides, they round-trip unchanged."""
    monkeypatch.chdir(tmp_path)
    env = {"MEMTOMEM_TOOL_MODE": "standard"}
    _write_mcp_json("uvx", ["--from", "memtomem", "memtomem-server"], env)

    data = json.loads((tmp_path / ".mcp.json").read_text(encoding="utf-8"))
    assert data["mcpServers"]["memtomem"]["env"] == env


def _make_test_profile(
    tmp_path: Path | None = None,
    *,
    kind: Literal["source", "project", "pypi"] = "pypi",
    mm_binary_origin: MmBinaryOrigin = "unknown",
    runtime_matches_workspace: bool | None = None,
) -> RuntimeProfile:
    """Build a :class:`RuntimeProfile` for tests without invoking
    :func:`_runtime_profile`.

    Replaces the pre-#368 pattern of setting
    ``state["source_install"] = True`` + ``state["source_dir"] = str(tmp_path)``
    — those legacy keys no longer exist after Phase 3 cleanup so tests that
    want a non-pypi profile seed ``state["_profile"]`` directly via this
    helper. Workspace venv detection mirrors :func:`_runtime_profile`:
    ``<cwd_install_dir>/.venv`` if it exists on disk.

    ``tmp_path`` is only meaningful when ``kind`` is ``"source"`` or
    ``"project"`` (it becomes ``cwd_install_dir``). For ``kind="pypi"`` it
    is ignored — callers may pass ``None`` or omit it.

    ``runtime_matches_workspace`` defaults to ``None`` which auto-computes
    via ``Path(sys.executable).is_relative_to(workspace_venv_path)`` — the
    same rule :func:`_runtime_profile` uses. Tests that monkeypatch
    ``sys.executable`` to point inside ``<tmp_path>/.venv`` then get ``True``
    automatically; explicit ``True``/``False`` force the flag regardless."""
    cwd_install_dir = tmp_path if kind in ("source", "project") else None
    venv = (cwd_install_dir / ".venv") if cwd_install_dir else None
    workspace_venv_path = venv if (venv and venv.exists()) else None
    runtime_interpreter = Path(sys.executable)

    matches: bool
    if runtime_matches_workspace is not None:
        matches = runtime_matches_workspace
    elif workspace_venv_path is not None:
        try:
            matches = runtime_interpreter.is_relative_to(workspace_venv_path)
        except (OSError, ValueError):
            matches = False
    else:
        matches = False

    return RuntimeProfile(
        cwd_install_type=kind,
        cwd_install_dir=cwd_install_dir,
        runtime_interpreter=runtime_interpreter,
        workspace_venv_path=workspace_venv_path,
        mm_binary_origin=mm_binary_origin,
        runtime_matches_workspace=matches,
    )


def _make_init_state(tmp_path: Path) -> dict:
    """Return a minimal wizard state dict pointing at *tmp_path*."""
    return {
        "provider": "none",
        "model": "",
        "dimension": 0,
        "db_path": str(tmp_path / "memtomem.db"),
        "memory_dir": str(tmp_path / "memories"),
        "enable_auto_ns": False,
        "default_ns": "default",
        "top_k": 10,
        "tokenizer": "unicode61",
        "decay_enabled": False,
        "rerank_enabled": False,
        # Non-config keys required by _write_config_and_summary
        "mcp_choice": 3,  # skip MCP setup
        "settings_hooks": False,
        # #368: legacy source_install / project_install / source_dir /
        # project_dir keys have been dropped — seed a default pypi profile
        # here so production code reads install-context via profile only.
        # Tests that need source/project classification overwrite
        # state["_profile"] via _make_test_profile(tmp_path, kind=...).
        "_profile": _make_test_profile(),  # default: pypi / unknown
    }


class TestInitConfigMerge:
    """mm init must preserve non-init fields on re-run."""

    def test_reinit_preserves_existing_mutable_fields(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """Post-init config changes (e.g. mmr.lambda_param) must survive re-init."""
        from memtomem.cli.init_cmd import _write_config_and_summary

        # Redirect ~/.memtomem to tmp_path/.memtomem
        monkeypatch.setenv("HOME", str(tmp_path))
        config_dir = tmp_path / ".memtomem"
        config_dir.mkdir()

        # First init
        state = _make_init_state(tmp_path)
        _write_config_and_summary(state, tmp_path)

        # User modifies config post-init (simulate mm config set)
        config_path = config_dir / "config.json"
        data = json.loads(config_path.read_text(encoding="utf-8"))
        data["mmr"] = {"enabled": True, "lambda_param": 0.3}
        data["search"]["bm25_candidates"] = 200
        config_path.write_text(json.dumps(data, indent=2), encoding="utf-8")

        # Re-run init (change provider)
        state["provider"] = "onnx"
        state["model"] = "all-MiniLM-L6-v2"
        state["dimension"] = 384
        _write_config_and_summary(state, tmp_path)

        result = json.loads(config_path.read_text(encoding="utf-8"))

        # Init fields updated
        assert result["embedding"]["provider"] == "onnx"
        assert result["embedding"]["dimension"] == 384

        # Non-init fields preserved
        assert result["mmr"]["enabled"] is True
        assert result["mmr"]["lambda_param"] == 0.3
        assert result["search"]["bm25_candidates"] == 200

    def test_first_init_works_without_existing_config(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """First init (no config.json) must work exactly as before."""
        from memtomem.cli.init_cmd import _write_config_and_summary

        monkeypatch.setenv("HOME", str(tmp_path))
        config_dir = tmp_path / ".memtomem"
        config_dir.mkdir()

        state = _make_init_state(tmp_path)
        _write_config_and_summary(state, tmp_path)

        config_path = config_dir / "config.json"
        data = json.loads(config_path.read_text(encoding="utf-8"))
        assert data["embedding"]["provider"] == "none"
        assert data["search"]["default_top_k"] == 10


